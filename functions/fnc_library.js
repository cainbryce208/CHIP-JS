/*
    Initiate functions library. This will be used to store all functions that are used in the project.
    Functions are registed to an object called chipFnc, which is then used to call the functions.
    This is done to avoid conflicts with other libraries.

    Example:
    chipFnc.functionName();

    Functions should keep a consistent naming convention, and should be included by category.
    Functions should be commented with a description, parameters, and return value.
    
    Example:
    // Description: This function adds two numbers together.
    // Parameters: num1 - The first number to add.
    //             num2 - The second number to add.
    // Return: The sum of num1 and num2.
    function add(num1, num2) {
        return num1 + num2;
    }

    For each new function category (to keep code organized), add a new folder under "functions" and include the functions in the new folder.
    In future versions, categories will be automatically included here but for now they must be manually included. 
    Create a new file for your functions and include it here.
*/
